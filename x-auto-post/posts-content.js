// 全投稿をフラットな配列で取得
export const getAllPosts = () => {
    return Object.values(posts).flat();
};

// カテゴリ別に投稿を取得
export const getPostsByCategory = (category) => {
    return posts[category] || [];
};

// ランダムに1件取得
export const getRandomPost = () => {
    const allPosts = getAllPosts();
    const randomIndex = Math.floor(Math.random() * allPosts.length);
    return allPosts[randomIndex];
};

// 指定カテゴリからランダムに1件取得
export const getRandomPostByCategory = (category) => {
    const categoryPosts = getPostsByCategory(category);
    if (categoryPosts.length === 0) return null;
    const randomIndex = Math.floor(Math.random() * categoryPosts.length);
    return categoryPosts[randomIndex];
};

export default posts;
